// Need custom function to turn off animations after certain time of inactivity
static uint32_t last_input_time = 0;

// Reset the timer on keypress
bool process_record_user(uint16_t keycode, keyrecord_t *record) {
    if (record->event.pressed) {
        last_input_time = timer_read32(); // Reset timeout
    }
    return true;
}

// Draw to OLED
bool oled_task_user() {
    if (is_keyboard_master()) {

        // ONLY want custom timeout for main side since right side will auto shutoff
        // Timeout
        if (last_input_time == 0) {
            last_input_time = timer_read32();
        }

        if (timer_elapsed32(last_input_time) > OLED_TIMEOUT) {
            oled_off(); // Turns off the OLED after timeout
            return false;
        }
        // Timeout End

        render_animation_left();
    } else {
        render_animation_right(); // Currently is just one image
    }

    return false;
}
